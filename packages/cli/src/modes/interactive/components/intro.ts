/**
 * One-shot ASCII intro shown on first launch.
 * Renders for ~2.5s then self-removes from the chat container.
 */
export function buildIntroLines(width: number): string[] {
	const steel = (s: string) => `\x1b[38;2;95;158;160m${s}\x1b[39m`;
	const dim = (s: string) => `\x1b[38;2;80;80;80m${s}\x1b[39m`;
	const bright = (s: string) => `\x1b[38;2;200;220;225m${s}\x1b[39m`;
	const gold = (s: string) => `\x1b[38;2;190;160;90m${s}\x1b[39m`;
	const red = (s: string) => `\x1b[38;2;180;70;70m${s}\x1b[39m`;

	// Big ASCII logo (7 rows)
	const logo = [
		steel("  в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—") + dim("  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—"),
		steel("  в–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘") + dim(" в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•”в•җв•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ"),
		steel("  в–Ҳв–Ҳв•”в–Ҳв–Ҳв–Ҳв–Ҳв•”в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘") + dim(" в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  "),
		steel("  в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘") + dim(" в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•қ  "),
		steel("  в–Ҳв–Ҳв•‘ в•ҡв•җв•қ в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘") + dim(" в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—"),
		steel("  в•ҡв•җв•қ     в•ҡв•җв•қ в•ҡв•җв•җв•җв•җв•җв•қ  в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•қ  в•ҡв•җв•җв•җв•қ") + dim("  в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•җв•қ"),
	];

	// Turkish flag crescent + star (right side accent)
	const flag = [
		red("        в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳ    "),
		red("      в–Ҳв–Ҳ      в–Ҳв–Ҳ  ") + gold(" вҳ…"),
		red("     в–Ҳв–Ҳ        в–Ҳ  "),
		red("      в–Ҳв–Ҳ      в–Ҳв–Ҳ  "),
		red("        в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳ    "),
	];

	// Tagline
	const tagline = bright("  En minimal.  En akДұllДұ.  En az token.  En sade.");
	const version = dim("  v2026-21  В·  github.com/theayzek01/MoonCode");
	const hint = dim("  /help  В·  /index  В·  /browser  В·  /ship  В·  Ctrl+C Г§ДұkДұЕҹ");

	// Separator
	const sep = dim("  " + "в”Җ".repeat(Math.min(width - 4, 70)));

	return ["", ...logo, "", sep, tagline, version, sep, hint, ""];
}
